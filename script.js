function showCategory(category) {
  const menuGrid = document.querySelector(".menu-grid");
  menuGrid.innerHTML = "";
  menuData[category].forEach((item) => {
    menuGrid.innerHTML += `
      <div class="menu-item">
  <!-- Սրտիկը -->
  <div class="favorite-icon" onclick="toggleFavorite(this, '${item.name}')">
    <i class="fa-regular fa-heart"></i>
  </div>
  <img src="${item.img}" alt="">
  <h3>${item.name}</h3>
  <p>${item.desc}</p>

  <div class="menu-actions">
    <div class="quantity-control">
      <button class="qty-btn" onclick="decreaseQty(this)">−</button>
      <span class="qty-value">1</span>
      <button class="qty-btn" onclick="increaseQty(this)">+</button>
    </div>
    <div class="price-value">${item.price.toLocaleString()} ֏</div>
  </div>

  <button class="add-to-cart">
    <i class="fa-solid fa-bag-shopping"></i> Ավելացնել
  </button>
</div>

    `;
  });
  

  

  document
    .querySelectorAll("nav.number a")
    .forEach((link) => link.classList.remove("active"));
  const activeLink = document.querySelector(
    `nav.number a[data-category="${category}"]`
  );
  if (activeLink) activeLink.classList.add("active");
}

window.onload = () => {
  showCategory("meat");
};


function increaseQty(button) {
  const qtySpan = button.parentElement.querySelector(".qty-value");
  let qty = parseInt(qtySpan.innerText);
  qtySpan.innerText = qty + 1;
}

function decreaseQty(button) {
  const qtySpan = button.parentElement.querySelector(".qty-value");
  let qty = parseInt(qtySpan.innerText);
  if (qty > 1) {
    qtySpan.innerText = qty - 1;
  }
}


let favoriteItems = [];

function toggleFavorite(el, itemName) {
  const icon = el.querySelector("i");
  const badge = document.querySelector(".top-icons .badge");

  icon.classList.toggle("fa-solid");
  icon.classList.toggle("fa-regular");

  if (favoriteItems.includes(itemName)) {
    favoriteItems = favoriteItems.filter(item => item !== itemName);
  } else {
    favoriteItems.push(itemName);
  }

  badge.textContent = favoriteItems.length;
}



function toggleFilter(element) {
  const box = element.parentElement;
  box.classList.toggle('closed');
}
